    public static string Text(string key, params object[] args)
    {
        var language = translations.TryGetValue(CurrentLanguage, out var languageMap)
            ? languageMap
            : translations[AppLanguage.English];
        var format = language.TryGetValue(key, out var value)
            ? value
            : translations[AppLanguage.English].TryGetValue(key, out var fallback)
                ? fallback
                : key;

        return args.Length == 0
            ? format
            : string.Format(CultureInfoFor(CurrentLanguage), format, args);
    }

    public static string RunModeLabel(SyncRunMode mode)
    {
        return mode == SyncRunMode.Transfer ? Text("SimpleTransfer") : Text("FullSynchronization");
    }

    public static string SourceKindLabel(SyncSourceKind sourceKind)
    {
        return sourceKind == SyncSourceKind.File ? Text("File") : Text("Folder");
    }

    public static CultureInfo CultureInfoFor(AppLanguage language)
    {
        try
        {
            return CultureInfo.GetCultureInfo(language.LocaleCode());
        }
        catch (CultureNotFoundException)
        {
            return CultureInfo.GetCultureInfo("en-US");
        }
    }

    private static AppLanguage DetectSystemLanguage()
    {
        var culture = CultureInfo.CurrentUICulture.Name;
        return AppLanguageExtensions.FromCode(culture);
    }

