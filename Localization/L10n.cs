public sealed record LanguageOption(string Code, string DisplayName, AppLanguage Language);

public static class AppLanguageExtensions
{
    public static string Code(this AppLanguage language)
    {
        return language switch
        {
            AppLanguage.Arabic => "ar",
            AppLanguage.Cantonese => "yue-Hant",
            AppLanguage.English => "en",
            AppLanguage.French => "fr",
            AppLanguage.German => "de",
            AppLanguage.Greek => "el",
            AppLanguage.Italian => "it",
            AppLanguage.Japanese => "ja",
            AppLanguage.Korean => "ko",
            AppLanguage.Polish => "pl",
            AppLanguage.Portuguese => "pt",
            AppLanguage.Spanish => "es",
            AppLanguage.Ukrainian => "uk",
            _ => "en"
        };
    }

    public static string LocaleCode(this AppLanguage language)
    {
        return language switch
        {
            AppLanguage.Arabic => "ar-SA",
            AppLanguage.Cantonese => "zh-HK",
            AppLanguage.English => "en-US",
            AppLanguage.French => "fr-FR",
            AppLanguage.German => "de-DE",
            AppLanguage.Greek => "el-GR",
            AppLanguage.Italian => "it-IT",
            AppLanguage.Japanese => "ja-JP",
            AppLanguage.Korean => "ko-KR",
            AppLanguage.Polish => "pl-PL",
            AppLanguage.Portuguese => "pt-PT",
            AppLanguage.Spanish => "es-ES",
            AppLanguage.Ukrainian => "uk-UA",
            _ => "en"
        };
    }

    public static AppLanguage FromCode(string? code)
    {
        var normalized = code?.Replace("_", "-", StringComparison.Ordinal).ToLowerInvariant();
        if (string.IsNullOrWhiteSpace(normalized))
        {
            return AppLanguage.English;
        }

        if (normalized.StartsWith("yue", StringComparison.Ordinal) ||
            normalized.StartsWith("zh-hk", StringComparison.Ordinal) ||
            normalized.StartsWith("zh-mo", StringComparison.Ordinal))
        {
            return AppLanguage.Cantonese;
        }

        return normalized switch
        {
            "ar" => AppLanguage.Arabic,
            "yue-hant" => AppLanguage.Cantonese,
            "en" => AppLanguage.English,
            "fr" => AppLanguage.French,
            "de" => AppLanguage.German,
            "el" => AppLanguage.Greek,
            "it" => AppLanguage.Italian,
            "ja" => AppLanguage.Japanese,
            "ko" => AppLanguage.Korean,
            "pl" => AppLanguage.Polish,
            "pt" => AppLanguage.Portuguese,
            "es" => AppLanguage.Spanish,
            "uk" => AppLanguage.Ukrainian,
            _ when normalized.StartsWith("ar-", StringComparison.Ordinal) => AppLanguage.Arabic,
            _ when normalized.StartsWith("de-", StringComparison.Ordinal) => AppLanguage.German,
            _ when normalized.StartsWith("el-", StringComparison.Ordinal) => AppLanguage.Greek,
            _ when normalized.StartsWith("en-", StringComparison.Ordinal) => AppLanguage.English,
            _ when normalized.StartsWith("es-", StringComparison.Ordinal) => AppLanguage.Spanish,
            _ when normalized.StartsWith("fr-", StringComparison.Ordinal) => AppLanguage.French,
            _ when normalized.StartsWith("it-", StringComparison.Ordinal) => AppLanguage.Italian,
            _ when normalized.StartsWith("ja-", StringComparison.Ordinal) => AppLanguage.Japanese,
            _ when normalized.StartsWith("ko-", StringComparison.Ordinal) => AppLanguage.Korean,
            _ when normalized.StartsWith("pl-", StringComparison.Ordinal) => AppLanguage.Polish,
            _ when normalized.StartsWith("pt-", StringComparison.Ordinal) => AppLanguage.Portuguese,
            _ when normalized.StartsWith("uk-", StringComparison.Ordinal) => AppLanguage.Ukrainian,
            _ => AppLanguage.English
        };
    }
}

